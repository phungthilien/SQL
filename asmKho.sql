CREATE DATABASE ASSIGNMENT

USE ASSIGNMENT
GO
IF OBJECT_ID('Account') IS NOT NULL
DROP TABLE Account
GO
CREATE TABLE Account
(
USERNAME NVARCHAR(30) NOT NULL ,
PASS NVARCHAR (30) NULL,
ROLES NVARCHAR(30) NULL,
CONSTRAINT PK_USERS PRIMARY KEY(USERNAME)
)
IF OBJECT_ID('Category') IS NOT NULL
DROP TABLE Category
GO
CREATE TABLE Category
(
ID INT IDENTITY(1,1) NOT NULL,
CategoryName NVARCHAR(50) NULL,
CONSTRAINT PK_Category PRIMARY KEY(ID)
)
IF OBJECT_ID('TopicAssignment') IS NOT NULL
DROP TABLE TopicAssignment
GO
CREATE TABLE TopicAssignment
(
ID INT IDENTITY(1,1) NOT NULL,
CategoryId INT NULL,
ProductName NVARCHAR(50) NULL,
CurrentPrice DECIMAL NULL,
Quantity INT NULL,
filmstudio nvarchar(50)null,
CONSTRAINT PK_TopicAssignment PRIMARY KEY(ID),
CONSTRAINT FK_Category_TopicAssignment FOREIGN KEY(CategoryId) REFERENCES Category
)
IF OBJECT_ID('InventoryLog') IS NOT NULL
DROP TABLE InventoryLog
GO
CREATE TABLE InventoryLog
(
ProductId int not null,
IEDate date not null ,
IoE bit null,
Quantity int null,
Price Decimal null,
CONSTRAINT PK_InventoryLog PRIMARY KEY(IEDate),
CONSTRAINT FK_InventoryLog_TopicAssignment FOREIGN KEY(ProductId) REFERENCES Category
)
